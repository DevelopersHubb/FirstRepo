#7.	Print table of 24, 50 and 29 using loop.

num1 = 24
num2 = 50
num3 = 29

print("Table of 24")
for x in range(1, 11):
    print(num1, "x", x, "=", num1*x)
print("\nTable of 50")
for x in range(1, 11):
    print(num2, "x", x, "=", num2*x)
print("\nTable of 29")
for x in range(1, 11):
    print(num3, "x", x, "=", num3*x)